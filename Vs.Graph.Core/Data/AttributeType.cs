namespace Vs.Graph.Core.Data
{
    public enum AttributeType
    {
        /// <summary>
        /// This type supports a currency value
        /// </summary>
        Currency,
        /// <summary>
        /// This type supports a date and time value
        /// </summary>
        DateTime,
        /// <summary>
        /// This type supports a numerical value that includes decimal places
        /// </summary>
        Decimal,
        /// <summary>
        /// This type supports a text value
        /// </summary>
        Integer,
        /// <summary>
        /// This type supports a text value
        /// </summary>
        Text
    }
}